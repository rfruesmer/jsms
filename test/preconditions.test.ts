import { checkState } from "@/preconditions";

// --------------------------------------------------------------------------------------------------------------------

test("throws if condition is false", () => {
    expect(() => {
        checkState(false);
    }).toThrow(Error);
});

// --------------------------------------------------------------------------------------------------------------------

test("doesn't throw if condition is true", () => {
    expect(() => {
        checkState(true);
    }).not.toThrow();
});

